const hello = function(name) {
  const message = `Hello, ${name}`;
  return message;
};

console.log(hello('Novian'));

// Arrow function

const helloo = names => {
  const message = `Hello, ${names}`;
  return message;
};

console.log(helloo('Dikong'));

// Simplify arrow function

const hellooo = namess => `Hello, ${namess}`;
console.log(hellooo('Andika'));
